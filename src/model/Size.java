package model;

public class Size {

	String line;
	int Nkw;
	int Nid;
	int Nop;
	int Nnv;
	int Nsl;
	
	public String getLine() {
		return line;
	}
	public void setLine(String line) {
		this.line = line;
	}
	public int getNkw() {
		return Nkw;
	}
	public void setNkw(int nkw) {
		Nkw = nkw;
	}
	public int getNid() {
		return Nid;
	}
	public void setNid(int nid) {
		Nid = nid;
	}
	public int getNop() {
		return Nop;
	}
	public void setNop(int nop) {
		Nop = nop;
	}
	public int getNnv() {
		return Nnv;
	}
	public void setNnv(int nnv) {
		Nnv = nnv;
	}
	public int getNsl() {
		return Nsl;
	}
	public void setNsl(int nsl) {
		Nsl = nsl;
	}
	
	
}
